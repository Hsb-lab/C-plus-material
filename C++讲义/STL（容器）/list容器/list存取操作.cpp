//list存取操作
//功能描述：对list容器中数据进行存取
//（list不支持随机访问迭代器，它支持的双向迭代器）

//函数原型：
//1.front();//返回第一个元素
//2.back();//返回最后一个元素

//总结：
//1.list容器中不可以通过[]或者at方式访问数据
//2.返回第一个元素――front
//3.返回最后一个元素――back

#include<iostream>
using namespace std;
#include<list>

void test01()
{
	list<int>L1;
	L1.push_back(10);
	L1.push_back(20);
	L1.push_back(30);
	L1.push_back(40);
	//list本质是链表，不是用线性空间存储数据，迭代器也是不支持随机访问的

	cout << "第一个元素为：" << L1.front() <<endl;
	cout << "最后一个元素为：" << L1.back() << endl;

	//迭代器是不支持随机访问的
	list<int>::iterator it = L1.begin();
	it++;//支持双向
	it--;
	cout << *it << endl;
}

int main()
{
	test01();
}